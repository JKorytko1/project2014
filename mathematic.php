<p><b>Обучение на нашей специальности обеспечивает:</b></p>

<p>- получение квалификации «инженер-математик-исследователь»;
<p>- широту и универсальность теоретической и практической подготовки, позволяющую одинаково успешно работать в самых различных областях 
научной и инженерной деятельности;</p>
<p>- глубокие знания фундаментальных и специальных дисциплин и умение применять полученные знания на практике;</p>
<p>- высокую восприимчивость к новым знаниям и владение навыками самостоятельной научно-исследовательской работы;</p>
<p>- высокую компетентность в области использования современной вычислительной техники, овладение современными информационными и 
компьютерными технологиями.</p>
<p>- трудоустройство по специальности на ведущих предприятиях г. Харькова: «Хартрон», «Коммунар», «Турбоатом».</p>
<p>Основные преимущества нашей специальности, выделяющие ее сегодня среди других, состоят в удачном сочетании сложившейся за долгие годы 
системы подготовки специалистов, преподавательских кадров высшей квалификации, четкой направленности на решение важнейших задач современного
 производства с многообразием возможных направлений приложения полученных знаний и навыков.</p>
 
