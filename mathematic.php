<p><b>Кафедра «Системы и процессы управления готовит специалистов высшей квалификации в различных областях создания новой техники, среди которых 
следует назвать:</b></p>

<p>- разработка высокоэффективных технологий программирования встроенных ЦВМ реального времени;</p>
<p>- создание различного электронного оборудования, начиная с вычислительных комплексов производительностью до миллиарда операций в секунду 
и кончая автомобильной электроникой и системой электронных платежей;</p>
<p>- разработка и производство современного электронного медицинского оборудования на основе компьютерных технологий;</p>
<p>- разработка и исследование систем регулирования паровых и газовых турбин;</p>
<p>- внедрение новых информационных технологий и разработка автоматизированных систем контроля и управления магистральными нефтегазопроводами; 
<p>- создание автоматических систем для управления подвижными объектами в космосе и на Земле.</p>
<br></br>
<p>Наших выпускников отличает широкая эрудиция и высокий профессионализм. Достаточно сказать, что многие из них связаны с созданием систем 
управления ракетой-носителем «Энергия», космическими аппаратами «Буран», «Квант», «Кристалл», серии метеоспутников, спутников связи. 
Уровень подготовки специалистов, выпускаемых нашей кафедрой, соответствует самым высоким мировым требованиям.</p>
