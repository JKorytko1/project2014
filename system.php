    <p><big><b><i>Системное проектирование</i></b></big> - это междисциплинарная методология построения интеллектуальных сред, предназначенных 
    для решения задач исследования и проектирования  сложных объектов(систем, процессов) разной физической  природы компьютерными 
    средствами и при активном участии людей (экспертов, аналитиков, инженеров, исследователей, проектантов и т.п.). Специалист по
    системному проектированию занимается  решением спектра задач, связанных со сбором полезных данных для поставленной цели, их а
    нализом, построением методов и моделей обработки  этих данных, разработкой вариантов структур и архитектурных решений сложных 
    объектов и выбором  оптимальных проектных решений, которые  реализуют цель проектирования. Объектом исследования в системном 
    проектировании являются разнообразные объекты, которые подпадают под  категорию сложных систем и процессов, такие, как информационные 
    среды (например, распределенные компьютерные системы, семантические  Веб- и Грид-сети), объекты научных исследований (например, 
    космические или биологические объекты), сложные технические системы (например,  интегральные электронные схемы или гибридные 
    электронно-механические системы), экологические системы, технологические или бизнес-процессы  и тому подобное. Таким образом, 
    результаты системного проектирования в одних случаях могут быть представлены в виде конкретных проектных  технических решений, 
    а в иных случаях - в виде определенных обобщений, прогнозов или рекомендаций, построенных на основании сбора и обработки 
     больших объемов данных и их интеллектуального анализа с использованием автоматизированных компьютерных  средств.</p>

