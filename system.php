    <p>Специальность 7.05010103 «Системное проектирование» создана в Украине в 2010 году и относится к бакалаврскому направлению 
    «Компьютерные науки». Преподавание специальных дисциплин 5-го курса будут проводить специалисты кафедры «Системы и процессы управления» 
    НТУ «ХПИ», работающие в области разработки информационных технологий и систем управления в авиакосмической отрасли и в области 
    математического моделирования сложных объектов и систем. В рамках двух семестров подготовки специалист в области системного 
    проектирования получит знания таких базовых предметов, как методы оптимизации, теория информации, теория принятия решений. 
    В области компьютерной подготовки и программирования будут изучены курсы создания сложных компьютерных систем, в том числе и экспертных. 
    На базе языка С++, который изучается в период подготовки бакалавра, студенты научатся создавать приложения для работы в сложных системах; 
    на базе курсов баз данных будут рассмотрены вопросы организации хранения и работы с данными в проектируемых системах. В рамках 
    специального курса будет изучена методология выбора и проектирования программных решений. Кроме того, в рамках специальных курсов 
    будут изучены курсы программирования на языке java и компьютерной графики. Дипломные работы специалистов по системному проектированию 
    будут посвящены разработке информационного обеспечения навигационных систем в гражданской авиации; информационных систем, обеспечивающих 
    работы баз и банков данных; информационного обеспечения систем управления технологическими процессами и т.д. </p>
